// Current language state
let currentLanguage = 'en';

// Language switching functionality
function toggleLanguage() {
    currentLanguage = currentLanguage === 'en' ? 'ar' : 'en';
    
    // Update HTML attributes
    const html = document.documentElement;
    html.lang = currentLanguage;
    html.dir = currentLanguage === 'ar' ? 'rtl' : 'ltr';
    
    // Add or remove Arabic class for styling
    document.body.classList.toggle('arabic', currentLanguage === 'ar');
    
    // Update all translatable elements
    const translatableElements = document.querySelectorAll('[data-translate]');
    translatableElements.forEach(element => {
        const key = element.getAttribute('data-translate');
        if (translations[currentLanguage][key]) {
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = translations[currentLanguage][key];
            } else {
                element.innerHTML = translations[currentLanguage][key];
            }
        }
    });
    
    // Update page title
    document.title = translations[currentLanguage]['page_title'];
    
    // Save language preference
    localStorage.setItem('preferredLanguage', currentLanguage);
}

// Load saved language preference on page load
function loadLanguagePreference() {
    const savedLanguage = localStorage.getItem('preferredLanguage');
    if (savedLanguage && savedLanguage !== currentLanguage) {
        toggleLanguage();
    }
}

// Preloader
window.addEventListener('load', function() {
    // Load language preference first
    loadLanguagePreference();
    
    setTimeout(function() {
        document.querySelector('.preloader').style.opacity = '0';
        setTimeout(function() {
            document.querySelector('.preloader').style.display = 'none';
            
            // Animate hero content after preloader
            const heroElements = document.querySelectorAll('.hero h1, .hero p, .hero .cta-btn');
            heroElements.forEach((el, index) => {
                setTimeout(() => {
                    el.style.opacity = '1';
                    el.style.transform = 'translateY(0)';
                }, 300 * index);
            });
        }, 500);
    }, 1500);
});

